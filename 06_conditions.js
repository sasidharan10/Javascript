// if-else conditions : 

let age = 40;
if (age === 20) {
    console.log(`age = ${age}`);
}
else if (age === 30) {
    console.log(`age = ${age}`);
}
else {
    console.log(`age = ${age}`);
}

// Switch case : 

let cup = 3;
switch (cup) {
    case 1:
        console.log(`cup = ${cup}`);
        break;
    case 2:
        console.log(`cup = ${cup}`);
        break;
    default:
        console.log(`cup = ${cup}`);
        break;
}

/*

"==" and "!=" are loose equality, they dont check for same data type.

"===" and "!==" are strict strict equality, the data type of both variables also needs to be matched

*/