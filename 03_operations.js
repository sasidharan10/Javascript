// Operators in JavaScript 

// 1) Unary Operators
var a = 5;
var b = 10;
a += b;
b -= a;
console.log("a: " , a);
console.log("b: " , b);
// 2) Binary Operators
var d = 10 + 5;
console.log("d: " , d);

// Arithmetic Operations : 

var num1 = 5;
var num2 = 2;

console.log("num1 + num2 : " , (num1 + num2));
console.log("num1 - num2 : " , (num1 - num2));
console.log("num1 * num2 : " , (num1 * num2));
console.log("num1 / num2 : " , (num1 / num2));
console.log("num1++: " , (num1++));
console.log("++num1: " , (++num1));
console.log("num1--: " , (num1--));
console.log("--num1: " , (--num1));
// Exponent = **
console.log("num1 ** num2 : " , (num1 ** num2));  // 2^5